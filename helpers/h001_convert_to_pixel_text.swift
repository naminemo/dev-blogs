func convertToPixelText(text: String) -> String {
    let uppercaseText = text.uppercased()
    var pixelTextLines: [String] = Array(repeating: "", count: 5)

    for char in uppercaseText {
        if let pixelRows = pixelFontMap[char] {
            for i in 0..<5 {
                pixelTextLines[i] += pixelRows[i] + "  "  // 加入間隔
            }
        } else {
            // 處理未知字元，例如顯示為問號
            for i in 0..<5 {
                pixelTextLines[i] += "?????" + "  "
            }
        }
    }

    return pixelTextLines.joined(separator: "\n")
}

